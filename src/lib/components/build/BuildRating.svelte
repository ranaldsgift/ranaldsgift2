<script lang="ts">
	import { CareerBuildsStore } from "$lib/stores/DataStores";
	import { invalidateAll } from "$app/navigation";
	import type { ICareerBuild } from "$lib/entities/builds/CareerBuild";
	import { getUserState } from "$lib/state/UserState.svelte";
	import { toast } from "svelte-sonner";
	import { ROOT_API_URL } from "$lib/data/constants/constants";

	type Props = {
		build: ICareerBuild;
	};

	const { build = $bindable() }: Props = $props();

	const userState = getUserState();

	let ratedByUser = $derived(userState.user?.ratedBuilds?.map((ratedBuild) => ratedBuild.id).includes(build.id) ?? false);
	let ratingCount = $derived(build.ratingsCount ?? 0);

	const handleRating = async () => {
		if (userState.user) {
			if (userState.user.id === build.user?.id) {
				toast("You can't rate your own build, silly!", {
					position: "bottom-center",
				});
				return;
			}

			const response = await fetch(`${ROOT_API_URL}/build/${build.id}/rate`, {
				method: "POST",
				headers: {
					"content-type": "application/json",
				},
			});

			const json = await response.json();

			if (response.ok) {
				if (json.rated) {
					userState.addRating(build);
					build.ratingsCount = (build.ratingsCount ?? 0) + 1;
					toast(`Thanks for rating ${build.user?.name}'s ${build.name}!`, {
						position: "bottom-center",
					});
				} else {
					userState.removeRating(build);
					build.ratingsCount = (build.ratingsCount ?? 1) - 1;
					toast(`Removed your rating of ${build.user?.name}'s ${build.name}.`, {
						position: "bottom-center",
					});
				}
				invalidateAll();
				if (build.id) {
					CareerBuildsStore.invalidateById(build.id);
				}
			} else {
				toast(json.error, {
					position: "bottom-center",
				});
			}
		}
	};
</script>

<div class="rating flex">
	<span class="rating-count" title="Number of ratings">{ratingCount}</span>
	<button
		data-rated={ratedByUser ? "true" : "false"}
		class="rating-icon my-[-4px]"
		title="Rating"
		onclick={handleRating}
		aria-label="Rating"
	></button>
</div>

<style>
	.rating-count {
		align-self: center;
		font-size: 2rem;
	}
	.rating-icon {
		background: url("/images/icons/rating-icon.png");
		background-size: contain;
		background-repeat: no-repeat;
		width: 62px;
		height: 64px;
		display: block;
	}
	.rating-icon:hover,
	.rating-icon[data-rated="true"] {
		background: url("/images/icons/rated-icon.png");
		cursor: pointer;
	}
</style>
